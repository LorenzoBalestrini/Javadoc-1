package it.develhope.javadoc1;

/**
 * This class creates the game
 * @author Lorenzo Balestrini
 */
public class WordGames {

    /**
     * This method prints "Hello " with your word
     * @param word You can write here your word
     * @return Hello + word
     */
    public String addHelloWord(String word){
        return "Hello " + word;
    }

    /**
     * This method prints your full name
     * @param name insert your name
     * @param surname insert your surname
     * @return name + surname
     */
    public String getFullName (String name, String surname){
        return name + " " + surname;
    }
}
